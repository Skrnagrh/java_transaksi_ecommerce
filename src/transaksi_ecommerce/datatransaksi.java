    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Ekspor File Ke Microsoft Excel
        FileWriter file;
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new File("export_output/excel"));
        int retrival = chooser.showSaveDialog(null);
        if (retrival == JFileChooser.APPROVE_OPTION){
            try{
                TableModel model = Tabledata.getModel();
                file = new FileWriter(new File(chooser.getSelectedFile() +".xls"));

                for(int i=0; i < model.getColumnCount(); i++){
                    file.write(model.getColumnName(i).toUpperCase() + "\t");
                }
                file.write("\n");
                for(int i=0; i < model.getRowCount(); i++){
                    for(int j=0; j < model.getRowCount(); j++){
                        file.write(model.getValueAt(i,j).toString() + "\t");
                    }
                    file.write("\n");
                }
                file.close();
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

