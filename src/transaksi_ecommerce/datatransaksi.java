    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        //Menghapus Satu Baris Atau Satu Kolom Yang Ada di Tabel
        try{
            int baris = Tabledata.getSelectedRow();
            baris = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Menghapus Data?", "Konfirmasi", JOptionPane.YES_NO_CANCEL_OPTION);
            if (baris == 0){
                String nomor = Tabledata.getValueAt(baris, 0).toString();
                String SQL = "DELETE FROM table_uas WHERE Id= '"+nomor+"'";
                java.sql.Connection c= (Connection)koneksi.getConnection();
                java.sql.PreparedStatement pst= c.prepareStatement(SQL);
                pst.execute();
                JOptionPane.showMessageDialog(this, "Berhasil DIhapus");
            }else{
                JOptionPane.showMessageDialog(this, "Gagal DIhapus");
            }
        }catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        table();
        selectData();
    }//GEN-LAST:event_jButton5ActionPerformed

